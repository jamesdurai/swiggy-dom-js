function add(e) {
  e.parentElement.childNodes.forEach(function (cv) {
  if(cv.classList.contains('leastVal')){
    var a = parseInt(cv.innerHTML)
    cv.innerHTML=++a
      }
    })
}

function sub(e) {
  e.parentElement.childNodes.forEach(function (cv) {
  if(cv.classList.contains('leastVal')){
    var a = parseInt(cv.innerHTML)
   if(a>0){
      cv.innerHTML=--a
    }
      }
      
    })
    
}

